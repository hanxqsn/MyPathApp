package com.example.asus.pathapp;

public class BannerInfo {
    private String imgUrl;
    private String name;

    public BannerInfo(){

    }

    public String getImgUrl() {
        return imgUrl;
    }

    public String getName() {
        return name;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public void setName(String name) {
        this.name = name;
    }
}
